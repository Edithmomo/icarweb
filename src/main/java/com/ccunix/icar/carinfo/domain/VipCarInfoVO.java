package com.ccunix.icar.carinfo.domain;

import java.io.Serializable;
import java.util.Date;

public class VipCarInfoVO implements Serializable{
	private String cart_number;//车牌号
	private String vip_name;//会员名
	private String vip_phone;//电话
	private Date start_time;//开始时间
	private Date end_time;//结束时间
	private String start_time_str;//开始时间
	private String end_time_str;//结束时间
	public VipCarInfoVO() {

	}
	public VipCarInfoVO(String cart_number, String vip_name, String vip_phone,
			Date start_time, Date end_time, String start_time_str,
			String end_time_str) {
		this.cart_number = cart_number;
		this.vip_name = vip_name;
		this.vip_phone = vip_phone;
		this.start_time = start_time;
		this.end_time = end_time;
		this.start_time_str = start_time_str;
		this.end_time_str = end_time_str;
	}
	public String getCart_number() {
		return cart_number;
	}
	public void setCart_number(String cart_number) {
		this.cart_number = cart_number;
	}
	public String getVip_name() {
		return vip_name;
	}
	public void setVip_name(String vip_name) {
		this.vip_name = vip_name;
	}
	public String getVip_phone() {
		return vip_phone;
	}
	public void setVip_phone(String vip_phone) {
		this.vip_phone = vip_phone;
	}
	public Date getStart_time() {
		return start_time;
	}
	public void setStart_time(Date start_time) {
		this.start_time = start_time;
	}
	public Date getEnd_time() {
		return end_time;
	}
	public void setEnd_time(Date end_time) {
		this.end_time = end_time;
	}
	public String getStart_time_str() {
		return start_time_str;
	}
	public void setStart_time_str(String start_time_str) {
		this.start_time_str = start_time_str;
	}
	public String getEnd_time_str() {
		return end_time_str;
	}
	public void setEnd_time_str(String end_time_str) {
		this.end_time_str = end_time_str;
	}
	


}
