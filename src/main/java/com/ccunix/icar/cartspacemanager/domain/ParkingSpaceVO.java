package com.ccunix.icar.cartspacemanager.domain;

import java.io.Serializable;

public class ParkingSpaceVO implements Serializable {
	/**
	 * 车位表id
	 */
	private int parking_id;
	/**
	 * 总车位
	 */
	private int all_num;
	/**
	 * 已使用的车位
	 */
	private int use_num;
	/**
	 * 剩余车位
	 */
	private int residue;
	/**
	 * 增加 或 减少的车位数
	 */
	private int num;
	/**
	 * 增加 或 减少的使用的车位
	 */
	private int change_use_num;

	public ParkingSpaceVO() {
	}

	public ParkingSpaceVO(int parking_id, int all_num, int use_num,
			int residue, int num, int change_use_num) {
		this.parking_id = parking_id;
		this.all_num = all_num;
		this.use_num = use_num;
		this.residue = residue;
		this.num = num;
		this.change_use_num = change_use_num;
	}

	public int getParking_id() {
		return parking_id;
	}

	public void setParking_id(int parking_id) {
		this.parking_id = parking_id;
	}

	public int getAll_num() {
		return all_num;
	}

	public void setAll_num(int all_num) {
		this.all_num = all_num;
	}

	public int getUse_num() {
		return use_num;
	}

	public void setUse_num(int use_num) {
		this.use_num = use_num;
	}

	public int getResidue() {
		return residue;
	}

	public void setResidue(int residue) {
		this.residue = residue;
	}

	public int getChange_use_num() {
		return change_use_num;
	}

	public void setChange_use_num(int change_use_num) {
		this.change_use_num = change_use_num;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}
	

}
